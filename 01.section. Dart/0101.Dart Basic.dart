      ? '아직 시작 전'
      : '🔥 $streakDays일째';
  
  return '''
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
🎯 $name님의 학습 여정
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
목표: $goal
일일 카드: $dailyTarget장
연속 학습: $streakText
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
오늘도 시작해볼까요? 화이팅!''';
}

