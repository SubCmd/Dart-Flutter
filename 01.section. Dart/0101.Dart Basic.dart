// 문제 3: 다음 코드의 출력을 예측하고, DartPad에서 검증하시오.

void main() {
  int a = 10;
  int b = 3;
  print('a + b = ${a + b}');
  print('a / b = ${a / b}');
  print('a ~/ b = ${a ~/ b}');
  print('a % b = ${a % b}');
}

/*
* a + b = 13
* a / b = 3.3333333333333335
* a ~/ b = 3
* a % b = 1
*/

